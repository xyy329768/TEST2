//鼠标点击弹出收回div
function testphoto() {
	var photos = document.getElementById("photos");

	if(photos.style.display == "none") { //是隐藏的
		photos.style.display = "block"; //显示
	} else { //显示的
		photos.style.display = "none"; //隐藏
	}
}
