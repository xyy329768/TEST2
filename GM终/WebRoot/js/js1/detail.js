/*搜索框*/
function test1() {
	//清空文本框
	var msearch = document.getElementById("msearch");
	if(msearch.value == "搜索 天猫 商品/品牌/店铺") {
		msearch.value = "";
	}
}

function test2() {
	//恢复文本框中的内容
	var msearch = document.getElementById("msearch");
	if(msearch.value == "") {
		msearch.value = "搜索 天猫 商品/品牌/店铺";
	}
}
